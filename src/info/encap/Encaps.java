package info.encap;

//POJO class

public class Encaps {
	
	private int idNO;
	
	private String name;
	
	private long PhNo;

	public int getIdNO() {
		return idNO;
	}

	public void setIdNO(int idNO) {
		this.idNO = idNO;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getPhNo() {
		return PhNo;
	}

	public void setPhNo(long phNo) {
		this.PhNo = phNo;
	}
	
	
	
	

}
